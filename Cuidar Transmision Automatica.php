<p>Saber como cuidar la transmision automatica es muy importante si quieres evitar gastar un dineral en reparaciones y mantenimientos, tener los siguientes cuidados que te mencionaremos te ayudara a que desempeñe de forma optima y no presente fallos por falta de mantenimiento o mal uso.<br>
<strong>Nota:</strong> Una reparación de transmision automatica puede ser igual o mas costosa que ajustar un motor, por la electrónica y los componentes de precisión que integra.</p>
    
  
  <h2><span style="color: #0000ff;"><strong>Revisa el líquido de transmisión periódicamente.</strong></span></h2>
  
<p>La gran mayoria de los autos de transmision automatica, cuentan con una bayoneta para revisar el nivel de aciete, este además de indicarte el nivel correcto te ayudara a determinar el estado del mismo aceite.
<br>
Si el nivel del líquido es muy bajo, el color del aceite es oscuro o huele a quemado, te recomendamos ingresarlo a tu taller de confianza para que revise un profesional y anticipe si hay algun problema en la transmision automatica del auto.
</p>

<h2><span style="color: #0000ff;"><strong>Usa el aceite correcto para cuidar tu transmision automatica.</strong></span></h2>

<p>
Siempre consulta el manual del propietario de tu vehículo para determinar qué tipo de líquido de transmisión debe usar. Si después de hacerlo aún no estás seguro, comunícate con tu fabricante o pregúntale a un profesional.</p> 
  
<h2><span style="color: #0000ff;"><strong>Cuidar el aceite de la transmision automatica.
</strong></span></h2>
<p>
Se recomienda reemplazar el aceite de transmision automatica cada 40mil kms a como maximo cada 60mil kms, ya que la fricción y la temperatura ocasiona que pierda eficiencia el aceite de la transmision automatica.</p>
 
<h2><span style="color: #0000ff;"><strong>No apliques de forma inmediata la velocidad al encender el auto.</strong></span></h2>
 <p>
  Encender el auto y aplicar la velocidad de forma inmediata ocaciona desgaste prematuros ya que el aceite aun no lubrica en su totalidad la transmision automatica, ocacionando que esta de un golpe como consecuencia de ello. Al igual que el motor cuando enciendas el auto dale unos minutos y posteriormente aplica la velocidad.</p> 

<h2><span style="color: #0000ff;"><strong>Deja que tu auto se caliente antes de conducir.</strong></span></h2>
 
<p>Es recomendable que al encender tu motor en el primer trayecto del dia, le des unos 3 minutos antes de empezar tu recorrido para que tanto el motor como la transmision tomen una temperatura mas optima y recorra el sistema de lubricacion con mayor fluidez. Considera este consejo sobre todo en Invierno o cuando el auto pase varios dias sin ser encendido.
</p>

 <h2><span style="color: #0000ff;"><strong>Cuidar el Filtro de aceite de transmision automatica.
</strong></span></h2>
 
<p>Al igual que el aceite de transmision, es recomendable cambiar el filtro de aceite de la transmision automatica, por lo cual se recomienda cambiar este, cuando se cambie el aceite de la transmision. En algunos autos este no se puede reemplazar si no es desmontada la transmision automatica. Si este es tu caso considéralo a los 100mil kms para que lo reemplacen.
<br><br>
Con estos sencillos consejos lograras cuidar tu transmision automatica y evitar reparaciones costosas. Asi como una revision a tiempo con tu taller de confianza.</p>

