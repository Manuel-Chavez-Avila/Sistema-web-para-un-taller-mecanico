<p>Hoy en día podemos adquirir no solo autos a Gasolina o Diesel. También tenemos los autos hibridos, eléctricos, hidrogeno, entre otros. Pero pocas veces nos indican que objetivo tiene cada uno, así como los beneficios y contras que puede tener cada auto. Es por ello que hoy hablaremos de: Que es un Auto hibrido para que te permita tomar una mejor decisión al momento de adquirir uno.</p>
    
  
  <h2><span style="color: #0000ff;"><strong>QUE ES UN AUTO HIBRIDO</strong></span></h2>
  <br>

<p>Un Auto Hibrido esta impulsado por dos motores, que pueden ser de distinta tecnología, ya sea de combustión interna, eléctrico, gas licuado, hidrogeno, etc.
</p>

<h2><span style="color: #0000ff;"><strong>COMO FUNCIONA UN AUTO HIBRIDO</strong></span></h2>
<br>

<p>
Como te comentamos antes, un híbrido suele combinar un motor de gasolina y uno eléctrico. Su sistema de propulsión ofrece tres opciones diferentes de funcionamiento. El motor de gasolina mueve las ruedas, el motor eléctrico mueve las ruedas o ambos al mismo tiempo se encarga de mandar energía a las ruedas. También disponen de una caja de cambios, en este caso automática, ya sea de doble embrague, convertidor de par o de tipo CVT (transmisión continuamente variable).</p><br>
 

 
<h2><span style="color: #0000ff;"><strong>TIPOS DE AUTOS HÍBRIDOS
</strong></span></h2>
<p>
En general podemos clasificarlos en tres grupos que van de acuerdo con su funcionamiento:</p>
<br><br>
<p>1.- El auto <strong>hibrido en serie</strong> es el motor térmico el que no tiene conexión con las ruedas, sino que su cometido es generar electricidad para que el motor eléctrico mueva al vehículo. También se conocen como eléctricos de autonomía extendida, pues en realidad siempre se mueven gracias a la electricidad.<br><br>

2.- En el auto <strong>hibrido en paralelo</strong>, tanto el motor eléctrico como el de combustión interna (ya sea de gasolina o diésel) están conectados a las ruedas del vehículo.<br><br>

3.- Finalmente el auto <strong>hibrido combinado</strong> es cuando el auto se puede mover con el impulso de cualquiera de sus motores, ya que ambos tienen conexión mecánica con las ruedas, lo que permite circular en modo eléctrico. El motor eléctrico suele ser capaz de generar de energía en las frenadas y retenciones.<br><br></p>
  
<h2><span style="color: #0000ff;"><strong>LO MALO Y LO BUENO DE UN AUTO HIBRIDO</strong></span></h2>


<p>Aunque los autos híbridos tienen una trayectoria de 20 años en el mercado, todavía existe mucha desinformación sobre el funcionamiento de estos vehículos.
  <br><br>
 <strong>Desventajas:</strong><br><br>

1.-Las baterías no se reciclan apropiadamente pueden tener un alto impacto ambiental.<br><br>

2.-Suelen ser más caros que los modelos tradicionales.<br><br>

3.-Contar con dos tipos de motores puede incrementar su peso.<br><br>

4.-La reducción del consumo de combustible varía de acuerdo con el camino.<br><br>
</p>
<strong>Ventajas:</strong><br><br>
<p>1.-El consumo de combustible es mucho menor a los modelos tradicionales.<br><br>

2.-Son más eficientes.<br><br>

3.-Más silenciosos.<br><br>

4.-Su motor tiene una vida útil más larga.<br><br>

5.-Funciona con combustibles sumamente fáciles de encontrar en una gasolinera.<br><br>

6.-Las emisiones que emiten de contaminantes son menores.<br><br>

7.-No requieren un mantenimiento especial.<br><br>

</p>
<h2><span style="color: #0000ff;"><strong>CONCLUSION</strong></span></h2>

<p>Un auto hibrido se compone de dos tecnologías, cuyo objetivo es disminuir las emisiones contaminantes, así como el consumo de combustible.<br> Antes de comprar uno evaluar que el sistema hibrido funcione perfectamente ya que el mantenimiento correctivo suele ser mas caro que un motor convencional a gasolina.</p>



