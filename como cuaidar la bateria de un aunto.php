<p>Te has preguntado como cuidar la bateria del carro y que factores son los que ocasionan que esta se dañe o se acorte su tiempo de vida?. Es por ello que realizamos este articulo para que tu batería tenga un tiempo de vida mas prolongado. Y determines cuando esta requiere de reemplazo y puedas anticiparlo antes de que ya no encienda el carro y te deje varado en los lugares menos adecuados.</p><br><br>
    
  
  <h2><span style="color: #0000ff;"><strong>Principales Causas que Dañan la Bateria de tu Carro.</strong></span></h2>
<p>Dependiendo de los cuidados que reciba la bateria del carro será la durabilidad de la misma. Estos son los principales motivos por lo que la bateria se daña.

<br><br>
<p>1.-Dejar luces encendidas o aparatos conectados a la toma de corriente mientras el auto este apagado.<br><br>
2.-No encender el auto por periodos prolongados.<br><br>
3.-Colocar en el piso la bateria propicia que se descargue con mayor rapidez.  Si decides desconectarla es recomendable almacenarla en un lugar seguro, fuera del alcance de los niños y donde no este expuesta a temperaturas menores de 20°C o mayores a 35°C<br><br><br>
4.-No dejar el estéreo encendido si el motor del auto esta apagado.<br><br></p>

   

<h2><span style="color: #0000ff;"><strong>Avisos de Cuando la bateria del carro requiere reemplazo.</strong></span></h2>
<p>
Si la batería se infla es un síntoma de que requiere reemplazo.<br><br>

Mal olor, si la bateria genera un olor a azufre, quiere decir que ya esta dañada.<br><br>

Si al encender el auto por las mañanas se escucha débil la marcha, o solo hace un ruido de clack y al pasarle corriente enciende quiere decir que la bateria ya no retiene carga.<br><br>

Si tu auto tiene indicador del estado de la bateria y este marca mas de 14.9volts o menos de 12volts. Es recomendable efectuar una revision con un electrico automotriz, ya que la bateria o el alternador pueden presentar algún problema.
<br><br>
 
<h2><span style="color: #0000ff;"><strong>Mantenimiento Preventivo a la bateria. 
</strong></span></h2>
<p>
Aunque se diga que las baterías son libre de mantenimiento. Al 80% aun se les puede realizar un mantenimiento preventivo y este consta de colocarle liquido en la parte superior. Para lo cual recomendamos que lo haga un especialista electrico o mecanico automotriz ya que este se concidera un acido que puede afectar tu salud.</p>
  <br><br>
 
<h2><span style="color: #0000ff;"><strong>Solicita una Revisión Eléctrica</strong></span></h2>
<p>
  Es recomendable que una vez al año se revise el regimen de carga de tu batería, ya que con esto puede determinar si hay fugas de corriente. Lo que equivale que la batería se descargue y se dañe de forma prematura. Esta revisión la puede realizar un taller eléctrico y mecánico.</p>
 
<br><br>
 
<h2><span style="color: #0000ff;"><strong>Duración de una bateria Automotriz.</strong></span></h2>

<p>Una batería tiene un tiempo de vida que oscila entre los 3 a 5 años dependiendo de la marca y cuidados que reciba.
</p>
<br><br>
 <h2><span style="color: #0000ff;"><strong>Cada auto lleva una bateria especifica.
</strong></span></h2>
<p>Cada carro lleva una batería en especifico debido al: tamaño, ubicación de los polos y amperaje. El colocar una batería que no es compatible puede ocasionar daños, no solo se vera limitada su durabilidad si no también puede afectar el sistema eléctrico del auto. Para colocar la batería correcta es necesario solicitarla con los datos específicos de tu auto en tu tienda de auto servicio o refaccionaria. Estos datos son: Marca, Modelo, Versión y Año del auto</p>

<br><br>
<h2><span style="color: #0000ff;"><strong>Que significa CA, CCA Y CR en la bateria de tu carro</strong></span></h2>

<p>Ten en cuenta estos términos al comprar una bateria ya que si vives en una zona fría te serán de mucha utilidad.
</p>
<br>
<p class="style9"><b>Capacidad de arranque (CA)</b> Cantidad de amperios que la batería usa para accionar el motor de arranque en condiciones de 0° C. por ejemplo 450 C</p>
<br>
<p class="style9"><b>Capacidad de arranque en frío (CCA)</b> Cantidad de amperios que la batería utiliza para accionar el motor de arranque en condiciones de -18° C. por ejemplo 550 CCA</p>
<br>
<p class="style9"><b>Capacidad de reserva: (CR) </b>Determina los minutos que la batería continuará suministrando una corriente eléctrica constante, a plena carga y temperatura.</p>
<br>

<h2><span style="color: #0000ff;"><strong>Conclusión Como cuidar la Bateria del Carro</strong></span></h2>

<p>Si quieres que tu bateria dure la mayor cantidad de tiempo posible, te recomendamos lo siguiente:
  <br><br>
1.-Si no vas a ocupar el auto por un periodo prolongado, desconéctala y almacénala en un lugar seguro.<br><br>

2.-Si vas a utilizar el auto una vez por semana, procura encenderlo cada tercer día al menos 10 minutos o si tienes tiempo rocura darle una vuelta por las calles de tu localidad.<br><br>

3.-No dejes el estéreo, luces encendidas o aparatos conectados a la toma de corriente, mientras el auto este apagado.<br><br>

4.-Procura una vez al año realizar una revisión del regimen de carga con tu eléctrico o taller de confianza.<br>
<br>
5.-Si tu bateria lo permite revisa una vez al año el nivel del liquido o solicítalo con tu taller de confianza. <br><br>(ten cuidado este liquido es acido y puede dañar tu salud).<br><br>

6.-En tempordas de FRIO es importante que enciendas tu auto con mayor frecuencia si este no lo vas a utilizar con regularidad, ya que las bajas temperaturas descargan la bateria.<br><br>

7.-Coloca una funda aislante para baterias el cual mantendra tu bateria a una temperatura sana. Los climas extremos dañan las baterias!!!<br><br>
</p>
