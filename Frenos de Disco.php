  <h2><span style="color: #0000ff;"><strong>Que son los Frenos de Disco</strong></span></h2>

<p>Los frenos son un el sistema de seguridad activa más importante del coche. Por este motivo debemos vigilar el estado de nuestro sistema de frenado. En la actualidad el sistema de frenado de disco son los más comunes en nuestros vehículos. <br>

   

<h2><span style="color: #0000ff;"><strong>Frenos de Disco vs Tambor</strong></span></h2>

<p>
 El sistema de frenado con con disco es por mucho el mas efectivo y seguro. La mayoria de los autos actuales cuenta con frenos de disco en las 4 ruedas, tanto que se considera una nomra de seguridad junto con el ABS en  algunos paises. Es poco comun ver un auto que no tenga disco en la parte frontal, aun se encuentran combinaciones de disco en la parte frontal y tambor en la parte trasera.

</p>

 
<h2><span style="color: #0000ff;"><strong>Por que los discos van en la parte frontal?</strong></span></h2>

<p>
Quizás te hayas percatado que tanto autos como motocicletas la llevan freno de disco en la parte frontal y ¿Por qué? Porque durante una detención, todo el peso del coche recae hacia adelante y, por ende, sobre las ruedas anteriores.</p>
  
 
<h2><span style="color: #0000ff;">Ventajas y Desventajas de los Frenos de Disco</span></h2>

<p class="style9"><b>Ventajaa:</b>
1.- Son más ligeros que los frenos de tambor.<br>
2.- Tienen mejor sistema de enfriamiento.<br>
3.- Proporcionan un frenado mas uniforme y preciso.<br>
4.- Por diseño, son frenos autoajustables.<br>

<p class="style9"><b>Desventajas:</b>
1.- Son más caros que los frenos de tambor.<br>
2.- se requiere rectificar el disco o reemplazar en cada reemplazo de balatas.<br>
3.- El liquido de frenos debe cambiarse con mayor frecuencia.<br>
4.- Se requiere una mayor presión del pedal para detener el vehículo.<br>

 
<h2><span style="color: #0000ff;"><strong>Conclusión</strong></span></h2>

<p>Un auto con frenos de disco sera mas eficiente y seguro al momento de frenar.</p> 
 
