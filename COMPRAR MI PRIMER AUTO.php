	<h2><span style="color: #0000ff;"><strong>Mi Primer Auto</strong></span></h2>
<p>Llega una edad en la que nos realizamos la pregunta ¿Cual debe ser mi primer auto que debo comprar? <br>

Elegir tu primer auto va más haya que escoger una marca o modelo, es encontrar a ese compañero de aprendizaje con el que vivirás nuevas aventuras y que también te traerá nuevas y buenas responsabilidades.</p>

<p>Tu primer auto debe ayudarte en ser mas independiente y responsable. Es por ello que debes considerar tus necesidades, presupuesto y gusto. Si solo nos basamos en una tendencia sin evaluar lo anterior puede convertirse en un dolor de cabeza en el consumo de combustible, mantenimientos, etc.</p>

<h2><span style="color: #0000ff;"><strong>Como Seleccionar Mi Primer Auto</strong></span></h2>

<p>
	<strong>Necesidades:</strong> Ya que tengamos definidas nuestras necesidades y la etapa de vida en la que nos encontramos. Podemos determinar si el auto debe ser dos o cuatro puertas, si es un <a href="https://es.wikipedia.org/wiki/Sed%C3%A1n" target="_blank" rel="noopener">sedan</a> o un <a href="https://es.wikipedia.org/wiki/Hatchback" target="_blank" rel="noopener">hatchback</a>.</p>

<p>
	<strong>Presupuesto:</strong> Determinar un presupuesto es esencial ya que muchas veces inciamos con uno presupuesto para nuestro primer auto y terminamos un 20% por arriba de lo planeado. lo cual nos lleva a pedir prestado o solictar prestamos por arriba de nuestras capacidades. Considera un presupuesto con el cual no rebase tu capacidad de pago o el 30% de tu sueldo.</p>

<p>
	<b>Características</b><strong>&nbsp;del auto:</strong> Ya teniendo bien definida tus necesidades y presupuesto puedes determinar que tipo de auto necesitas. No es lo mismo comprar nuestro primer auto solo para ir a la escuela. Que una jeep para salir a la aventura en el bosque. O una minivan si no hijos y solo van abordar dos personas el auto.</p>
<p>Hay muchas marcas y estilos de vehículos, carros deportivos, SUV, Pickups, carros de lujo… En fin, todo depende de los gustos que tengas.</p>

<h2><span style="color: #0000ff;"><strong>Los gastos de un auto</strong></span></h2>

<p>
	En los gasto del auto no solo se encuentran <strong>el enganche y las mensualidades</strong> si lo compras de <strong>agencia</strong> y aun si lo compras de uso ,también conlleva gastos como los precios del <strong>mantenimiento</strong>, el costo de las <strong>placas</strong>, el <strong>seguro</strong>, la <strong>verificación</strong>, la <strong>tenencia</strong> y el consumo de <strong>gasolina</strong> que puede variar entre los distintos modelos de autos.</p>

<p>Además, también hay que pensar en gastos que parecerían <strong>poco relevantes</strong> pero que pueden terminar representando un gasto elevado. ¿Cuánto dinero vas a gastar en estacionamientos? ¿Ya tienes tu licencia? Este trámite también te va a costar.</p>

<h2><span style="color: #0000ff;"><strong>Donde Comprar Mi Primer Auto</strong></span></h2>

<p>Tu primer auto puedes adquirirlo basicamente de cuatro formas.<br>
1.- En una agencia de autos<br>
2.- En un bazar de autos<br>
3.- A travez de una plataforma web<br>
4.- Comprarlo a otro particular.<br>
Considera el riego beneficio que ofrece cada opcion, si buscas seguridad la opcion “mas segura” es adquirirlo en agancia.</p>
<h2><span style="color: #0000ff;">Mi Primer Auto Debe ser:<strong> Nuevo o Seminuevo</strong></span></h2>

<p>

	Al comprar un <strong>auto NUEVO</strong>, no te dejes llevar por anuncios, ni por el primer vendedor de agencia. Muchos concesionario de la misma marca, tienen variaciones de precio en el mismo auto que estas buscando. Busca en Internet referencias, visita diferentes agencias, pide una <strong>prueba de manejo</strong> y sobre todo preguntale a las personas que ya lo hayan comprado que tal les a funcionado el auto.</p>
<p>La ventaja de adquirirlo a travez de <strong>créditos automotrices</strong> es que puedes compararlos entre si. Realiza un estudio previo de cuáles son los que se adaptan mejor a tus necesidades específicas y cuáles son los que cuestan menos.</p>
<p>Los <strong>autos SEMINUEVOS</strong> son autos de segunda mano, es decir, que ya fueron vendidos a alguien cuando eran nuevos., si es un auto seminuevo, se trata de un vehículo que tiene menos de 50,000 km&nbsp; y menos de 5 años de uso.</p>
<p>Los <strong>autos USADOS</strong>, generalmente superan esas barreras. Estos solo se adquieren entre particulares o comprarse a empresas que hacen cambio de flotilla. Se debe tener mayor cuidado en este tipo de autos, ya que requieren de una buena revisión antes de efectuar la compra, te recomendamos revisarlo por tu taller mecánico de confianza.</p>
<p>Revisa que te entreguen todos los papeles correspondientes y que estén en regla (la tarjeta de circulación, las calcomanías, sus recibos de tenencia, los papeles de verificación vehicular, etc). Cerciórate de que tu contrato de compraventa este bien redactado.</p>
<h2><span style="color: #0000ff;"><strong>Mejor época del año para adquirirlo</strong></span></h2>

<p>
	<strong>&nbsp;</strong>Generalmente, las mejores ofertas son a <strong>finales de verano </strong>y <strong>principios de otoño</strong> cuando llegan los modelos del año siguiente.</p>

<p>Aunque <strong>diciembre</strong> es un mes que regularmente es muy pesado en cuestión de gastos, no es una mala opción para comprar un auto. Tratándose de un auto nuevo algunas agencias ofrecen descuentos para alcanzar sus metas ventas anuales y si es un auto de uso , por los gastos que pueda tener el vendedor en esas fechas.</p> 
