<p>Hoy en dia el precio de la gasolina no para de subir y pareciera que cada vez rinde menos. Es por ello que decidimos 5 sencillos trucos o mejor dicho consejos para ahorrar gasolina. Con la finalidad de que puedas optimizar el rendimiento, consumo y gasto de combustible.</p>
    
  
  <h2><span style="color: #0000ff;"><strong>Infla las Llantas de tu Auto Correctamente</strong></span></h2>

<p>Las llantas mal infladas son un problema que afectan no solo al desgaste de la mismas llantas. También afectan a toda la mecánica del auto ya que genera mayor esfuerzo todo el auto para moverse. Un ejercicio rápido es empuja un auto con llantas infladas a 25psi y empuja el mismo con llantas a 35psi. En el segundo caso el auto es mas suave y lo empujamos con mayor facilidad<br>

   

<h2><span style="color: #0000ff;"><strong>No Cargues Gasolina de a Cuartitos.</strong></span></h2>

<p>
El motor no solo usa la gasolina para trabajar, también usa los GASES que se generan dentro del tanque de gasolina. Los cuales al ser volátiles optimizan el funcionamiento del motor así como el rendimiento del mismo combustible. Un experimento rápido es llenar una botella transparente de medio litro de gasolina. Cerrarla bien y agitarla. Cuando la abres suena como si abrieras una soda. Este ruido de “TSSS” son los gases que se generan con la agitación del combustible. Lo mismo sucede dentro del tanque de gasolina, esos gases son recuperados por el motor para ser quemados en su interior.
<br>


</p>
<p>Lo cual beneficia al rendimiento y desempeño del motor. Por lo cual es recomendable cargar mínimo medio tanque y no un cuarto de gasolina. Mientras menor sea la cantidad de combustible es menor la cantidad de gases que se generan . Afectando el rendimiento de combustible y aumentando tu gasto en ello.</p>
 
<h2><span style="color: #0000ff;"><strong>En Carretera o Autopista Sube los Vidrios 
</strong></span></h2>

<p>
En carretera es habitual que mucha gente baja un poco los vidrios en vez de poner el aire acondicionado o peor aun poner el aire acondicionado y traer los vidrios abajo. Esto afecta a la aerodinámica del auto introduciéndose el aire al interior del auto, creando una bolsa en el interior, como si fuese un paracaídas abierto. Haciendo que el auto se frene y por ende tengamos que acelerar mas. Irónicamente el auto gasta menos gasolina con el aire acondicionado puesto con los vidrios cerrados que no llevar aire acondicionado y los vidrios abajo</p>
  
 
<h2><span style="color: #0000ff;">Frenar y Acelerar de Forma Constante e Innecesaria.</span></h2>

<p>
  Un mal habito de manejo es soltar el pedal del acelerador para inmediatamente pisar el pedal del freno. Este mal habito además de gastar mas rapido los frenos ocasiona que gastes mas gasolina por la perdida de inercia al frenar y volver acelerar.
<br> Esto se soluciona con el simple echo de mantener mayor distancia con el auto de enfrente y soltar el pedal del acelerador y el auto por ende va a perder impulso lo cual hace que descienda la velocidad si el auto de enfrente ya avanza no fue necesario frenar solo pisaremos el acelerador si la distancia ya es prudente para iniciar el desplazamiento con mayor velocidad. Es algo muy simple pero el ahorro en el trafico o en carretera es muy significativo. Solo mantén una buena distancia con el auto de enfrente que no comprometa tu seguridad o la de otros.</p>
 

 
<h2><span style="color: #0000ff;"><strong>Exceso de Equipaje o Triques en el interior del Auto.</strong></span></h2>

<p>Hay quienes usan el auto o la cajuela como bodega o ropero. Guardar cosas innecesarias dentro del auto genera mayor peso para que el auto se desplace. Sucede lo mismo que con las llantas desinfladas, a mayor peso que tenga el auto, este se tiene que esforzar mas para desplazarse. En el corto plazo poco se percibe, pero en el largo plazo genera mayor desgaste al motor asi como de consumo de gasolina.</p>

 <h2><span style="color: #0000ff;"><strong>Con estos 5 trucos para ahorrar gasolina.</strong></span></h2>
<p>Te darás cuenta que el rendimiento incrementa y el gasto en gasolina se disminuye.</p>
