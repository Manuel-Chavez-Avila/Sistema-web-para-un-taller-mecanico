  <h2><span style="color: #0000ff;"><strong>Tips para cuidar la pintura del auto.</strong></span></h2>
  
<p>La durabilidad y brillo de la pintura de nuestro auto esta relacionada con los cuidados y hábitos que tengamos con el. Por lo cual te damos los siguientes Tips para cuidar la pintura de tu Auto y prolongues su vida sin que pierda brillo. Además de que te evitara pagar costosos tratamientos especializados en pintura.
</p>

<h2><span style="color: #0000ff;"><strong>No lavar el auto bajo los rayos del sol</strong></span></h2>
 
<p>
Lavar el auto con la pintura caliente genera daños al brillo de la pintura, haciéndola opaca con el tiempo, además siendo mas susceptible a que esta se ralle por el echo de estar caliente la capa de pintura lo cual la vuelve mas vulnerable.</p> 
 
<h2><span style="color: #0000ff;"><strong>No lavarlo después de haberlo usado
</strong></span></h2>
 
<p>
Esto es lo mismo que punto anterior de lavarlo bajo el sol. Si acabas de usar tu auto la pintura del cofre va estar caliente por el calor que emana el motor. Es recomendable esperar a que la pintura se enfrié bien antes de ser rociado con agua. Ya que de lo contrario la pintura estarás acabando con el brillo de tu pintura.</p>
 
  
 
<h2><span style="color: #0000ff;"><strong>No uses productos inadecuados para lavarlo</strong></span></h2>
 <p>
  Este punto hace especial referencia en no usar productos para lavarlo como: shampoo para cabello, jabón lavavajillas. Así como fibras, cepillos o esponjas que no están diseñadas para la estetica automotriz, esto evitara que tu auto se raye o dañe el brillo de la pintura.</p> 

<h2><span style="color: #0000ff;"><strong>No colocarle una lona, si la pintura esta mojada</strong></span></h2>
 
<p>Es muy habitual que cuando empieza a llover se coloque una lona, hacer esto y colocar una lona después de lavarlo puede ocasionarle serios daños a la pintura. Ya que al cubrir la pintura del auto aun estando mojada, ocasionara humedad y si esta recibe los rayos del sol o calor, genera una mancha blanca en la pintura. la cual muchas veces solo se quita puliendo o pintando la pieza dañada.
</p>

 <h2><span style="color: #0000ff;"><strong>Encerar la pintura del auto
</strong></span></h2>
 
<p>La cera le ofrece al auto una protección extra a la pintura de nuestro auto, que funciona como un teflón para que el polvo y el agua no se pegue. Se recomienda encerar el auto cada 6 meses. Antes de comprar una cera revisa y consulta en tu tienda de confianza cual es la mas adecuada para la pintura de tu auto. Una cera por tradición y que es noble con la pintura es la Tempo clásica de SC-Jhonson, la cual se puede usar con pinturas de mas de 5 años.</p>


<h2><span style="color: #0000ff;"><strong>Pulir la pintura del auto</strong></span></h2>
 
<p>Se recomienda pulir la pintura del auto cada 3 a 5 años. Pulir la pintura es eliminar una pequeña capa de pintura que se daña por la intemperie (agua, sol, rayones, etc.) No se recomienda pulir el auto de forma contante ya que va adelgazando la capa de pintura transparente que tienen los autos. Pulir el auto se recomienda hacerlo con un especialista ya que el no hacerlo correctamente puedes adelgazar tanto la pintura en una mal pasada que la terminaría dañando.
 
<h2><span style="color: #0000ff;"><strong>No estacionar el auto bajo arboles</strong></span></h2>

<p>Los arboles además de llenar de hojas y ramas el auto, estos tapan los desagües que tiene incorporado el auto en puertas, quemacocos, cajuela, cofre, etc. si no nos percatamos a tiempo la acumulación de agua y tierra genera oxido dañando no solo la pintura si no hasta la misma pieza. El oxido es el peor enemigo de la pintura y del metal.</p>
   
<h2><span style="color: #0000ff;"><strong>CONCLUSION</strong></span></h2>

<p>Con estos tips para cuidar la pintura del auto lograras que la pintura te dure mas años sin perder su brillo.
Siempre que haya la posibilidad procura que el auto esté bajo sombra, pero tomando en cuenta lo anterior.
Resguarda el auto en el garaje, así evitas el desgaste por las condiciones climáticas y otras eventualidades.</p>

